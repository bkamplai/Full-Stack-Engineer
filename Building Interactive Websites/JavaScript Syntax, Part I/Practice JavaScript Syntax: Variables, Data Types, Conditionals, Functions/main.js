// canIVote()
function canIVote(age) {
    if (age >= 18) {
        return true;
    } else {
        return false;
    }
}

// agreeOrDisagree()
function agreeOrDisagree(str1, str2) {
    if (str1 === str2) {
        return 'You agree!';
    } else {
        return 'You disagree!';
    }
}