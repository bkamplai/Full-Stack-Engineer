// Repeating Tasks Manually
const vacationSpots = ['Ireland', 'Germany', 'United Kingdom'];
console.log(vacationSpots[0]);
console.log(vacationSpots[1]);
console.log(vacationSpots[2]);

// The For Loop
for (let i = 5; i < 11; i++) {
    console.log(i);
}

// Looping in Reverse
for (let counter = 3; counter >= 0; counter--) {
    console.log(counter);
}